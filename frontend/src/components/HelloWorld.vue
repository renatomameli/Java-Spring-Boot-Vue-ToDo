<template>
  <div class="hello">
    <div>
      <h1>Meine Todos</h1>
      <ul>
        <li v-for="todo in todos" :key="todo.id">{{ todo.title }}</li>
      </ul>
    </div>
  </div>
</template>

<script>
export default {
  data() {
    return {
      todos: [],
    };
  },
  methods: {
    async fetchTodos() {
      try {
        const response = await fetch("https://jsonplaceholder.typicode.com/todos");
        this.todos = await response.json();
      } catch (error) {
        console.error("Es gab ein Problem beim Abrufen der Todos:", error);
      }
    },
  },
  watch: {
    todos: {
      handler() {
        // Hier kannst du Code einf체gen, der ausgef체hrt wird, wenn sich `todos` 채ndert
        console.log("Todos wurden aktualisiert.");
      },
      deep: true,
    },
  },
  mounted() {
    this.fetchTodos(); // Todos abrufen, wenn die Komponente eingeh채ngt wird
  },
  name: 'HelloWorld',
  props: {
    msg: String
  }
}
</script>

